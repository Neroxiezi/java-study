public class Operator {
    public static void main(String[] args) {
        int a = 20;
        int b = 20;
        int c = 25;
        int d = 25;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a++ = " + (a++));
        System.out.println("--c = " + (--c));
        System.out.println("c-- = " + (c--));
        System.out.println("d++ = " + (d++));
        System.out.println("++d = " + (++d));
        System.out.println("c-- = " + (c--));
        System.out.println("--c = " + (--c));
    }
}
